# Programa para calcular a área de um retângulo

# Solicitar a base e altura do usuário
base = float(input("Digite o valor da base do retângulo: "))
altura = float(input("Digite o valor da altura do retângulo: "))

# Calcular a área
area = base * altura

# Exibir o resultado
print(f"A área do retângulo com base {base} e altura {altura} é: {area}")