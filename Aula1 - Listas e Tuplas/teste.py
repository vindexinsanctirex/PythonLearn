# Passo 1: Criar uma lista com seis frutas
lista_frutas = ["maçã", "banana", "laranja", "uva", "morango", "abacaxi"]

# Passo 2: Converter a lista em uma tupla
tupla_frutas = tuple(lista_frutas)

# Passo 3: Exibir o conteúdo da tupla resultante
print("Tupla de frutas:", tupla_frutas)
print("Tipo do objeto:", type(tupla_frutas))